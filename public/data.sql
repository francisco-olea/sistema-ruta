  CREATE TYPE renta.tipo_orden AS ENUM ('Obra', 'Evento', 'Otro');
EXCEPTION
  WHEN duplicate_object THEN null;
END $$;

DO $$ BEGIN
  CREATE TYPE renta.estatus_ruta AS ENUM ('pendiente', 'en_proceso', 'completado');
EXCEPTION
  WHEN duplicate_object THEN null;
END $$;

DO $$ BEGIN
  CREATE TYPE renta.metodo_pago AS ENUM ('Efectivo', 'Transferencia', 'Tarjeta');
EXCEPTION
  WHEN duplicate_object THEN null;
