  CREATE TYPE renta.estatus_pago AS ENUM ('pagado', 'pendiente', 'vencido');
EXCEPTION
  WHEN duplicate_object THEN null;
END $$;

CREATE TABLE IF NOT EXISTS renta.usuarios (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  username VARCHAR(100) NOT NULL UNIQUE,
  password_plain VARCHAR(255) NOT NULL,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS renta.clientes (
  id BIGINT PRIMARY KEY,
  nombre VARCHAR(255) NOT NULL,
  telefono VARCHAR(50) NOT NULL DEFAULT '',
  email VARCHAR(255) NOT NULL DEFAULT '',
  empresa VARCHAR(255) NOT NULL DEFAULT '',
  rfc VARCHAR(30) NOT NULL DEFAULT '',
  domicilio TEXT NOT NULL,
  created_at DATE NOT NULL
);

CREATE TABLE IF NOT EXISTS renta.productos (
  id BIGINT PRIMARY KEY,
  nombre VARCHAR(255) NOT NULL,
  descripcion TEXT NOT NULL,
  precio_renta NUMERIC(12,2) NOT NULL,
  stock INTEGER NOT NULL CHECK (stock >= 0),
  activo BOOLEAN NOT NULL DEFAULT TRUE,
  color VARCHAR(100),
  notas TEXT,
  eje VARCHAR(100),
  medida VARCHAR(100),
  tanques INTEGER,
  agua INTEGER,
  drenaje INTEGER,
  tablones INTEGER,
  ruedas INTEGER,
  tiempo INTEGER
);

CREATE TABLE IF NOT EXISTS renta.ordenes (
  id BIGINT PRIMARY KEY,
  cliente_id BIGINT,
  cliente_nombre VARCHAR(255) NOT NULL,
  tipo renta.tipo_orden NOT NULL,
  estado renta.estado_orden NOT NULL,
  renta NUMERIC(12,2) NOT NULL,
  producto VARCHAR(255) NOT NULL,
  cantidad INTEGER NOT NULL CHECK (cantidad > 0),
  ruta SMALLINT NOT NULL CHECK (ruta > 0),
  frecuencia VARCHAR(120) NOT NULL,
  domicilio TEXT NOT NULL,
  map_lat NUMERIC(10,7),
  map_lng NUMERIC(10,7),
  fecha_inicio DATE NOT NULL,
  fecha_fin DATE,
  notas TEXT NOT NULL DEFAULT '',
  created_at DATE NOT NULL,
  updated_at DATE NOT NULL,
  CONSTRAINT fk_orden_cliente
    FOREIGN KEY (cliente_id)
    REFERENCES renta.clientes(id)
    ON UPDATE CASCADE
    ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS renta.pagos (
  id BIGINT PRIMARY KEY,
  orden_id BIGINT NOT NULL,
  cliente_nombre VARCHAR(255) NOT NULL,
  monto NUMERIC(12,2) NOT NULL,
  metodo renta.metodo_pago NOT NULL,
  fecha DATE,
  concepto TEXT NOT NULL,
  estatus renta.estatus_pago NOT NULL,
  CONSTRAINT fk_pago_orden
    FOREIGN KEY (orden_id)
    REFERENCES renta.ordenes(id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS renta.registros_ruta (
  id BIGINT PRIMARY KEY,
  orden_id BIGINT NOT NULL,
  cliente VARCHAR(255) NOT NULL,
  ubicacion TEXT NOT NULL,
  map_lat NUMERIC(10,7),
  map_lng NUMERIC(10,7),
  notas TEXT NOT NULL DEFAULT '',
  estatus renta.estatus_ruta NOT NULL,
  evidencia1 TEXT,
  evidencia2 TEXT,
  evidencia3 TEXT,
  evidencia4 TEXT,
  evidencia5 TEXT,
  firma TEXT,
  hora_firma TIME,
  ruta SMALLINT NOT NULL CHECK (ruta > 0),
  dia VARCHAR(20) NOT NULL,
  fecha DATE NOT NULL,
  CONSTRAINT fk_registro_orden
    FOREIGN KEY (orden_id)
    REFERENCES renta.ordenes(id)
    ON UPDATE CASCADE
    ON DELETE CASCADE
);

CREATE INDEX IF NOT EXISTS idx_ordenes_cliente_id ON renta.ordenes (cliente_id);
CREATE INDEX IF NOT EXISTS idx_ordenes_ruta ON renta.ordenes (ruta);
CREATE INDEX IF NOT EXISTS idx_ordenes_estado ON renta.ordenes (estado);
CREATE INDEX IF NOT EXISTS idx_pagos_orden_id ON renta.pagos (orden_id);
CREATE INDEX IF NOT EXISTS idx_pagos_estatus ON renta.pagos (estatus);
CREATE INDEX IF NOT EXISTS idx_registros_ruta_orden_id ON renta.registros_ruta (orden_id);
CREATE INDEX IF NOT EXISTS idx_registros_ruta_fecha ON renta.registros_ruta (fecha);

